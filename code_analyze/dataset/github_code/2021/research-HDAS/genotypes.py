def parse_concat(beta):
    """
    parse continuous beta ti discrete concat
    beta is ParameterList:
    ParameterList [
        Parameter(1, 1),
        Parameter(1, 1),
        ...
    ]

    concat is list:
    range(2, 4)
    range(5, 7)
    ...
    range(6, 8)
    """
    _, index = torch.topk(beta, 1, dim=0)
    return range(index + 4, index + 6)
